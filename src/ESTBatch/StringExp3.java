package ESTBatch;

public class StringExp3 {

	public static void main(String[] args) {
		String name1 = "NewYork City";
		char let = name1.charAt(5);
		System.out.println(let);
		char nim = name1.charAt(0);
		System.out.println(nim);
		//print all char in given String
		for(int i = 0; 1<name1.length(); i++)
		{
			char let1 = name1.charAt(i);
			System.out.println(let1);
		}

	}

}
