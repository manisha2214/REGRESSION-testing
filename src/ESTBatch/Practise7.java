package ESTBatch;

public class Practise7 {
	
	public static void main(String[]args) {
		//switch statement in java
		int score = 90;
		// expresion can be byte,short,int or char.
		switch (score)
		{
		case 90:
			System.out.println("Very good");
			break;
		case 60:
			System.out.println(" good");
			break;
		case 40:
			System.out.println("ok");
			break;
		case 20:
			System.out.println("Not good");
			break;
			default:
				System.out.println("The grads are not defined");
				break;
			
		}
	}

}
