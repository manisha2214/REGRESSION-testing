package ESTBatch;

public class ConstructorExp1 {
	public ConstructorExp1()
	{
		System.out.println("This is constructor without any arguments");
		
	}
	
	public void Simple()
	{
		
		System.out.println("Hello");
		
	}
	
	
	public static void main(String[] args) {
		ConstructorExp1 obj = new ConstructorExp1();
		obj.Simple();

	}

}
